public class 출력문_01 {
    public static void main(String[] args) {
        /*println소괄호 안의 내용을 출력함 + 한 줄 개행*/
        System.out.println("hello~");
        System.out.println("hello~");

        /*그냥 print는 소괄호 안의 내용만 출력*/
        System.out.print("hello~");
        System.out.print("hello~");

        /*한 줄 개행*/
        System.out.println();

        /*숫자를 출력할때는 쌍따옴표를 넣지 않는다.*/
        /*문자는 반드시 쌍따옴표로 감싼다.*/
        /*쌍따옴표가 붙으면 글자로 인식한다 연산이 되지 않음*/
        System.out.println(5);
        System.out.println("5");


    }
}
