public class 출력문_02 {
    /*main+enter*//*클래스 이름은 무조건 대문자*/
    /*코드는 위에서부터 아래로 해석된다.*/
    /*코드는 'main 메서드'의 위에서부터 아래로 해석된다.*/

    public static void main(String[] args) {
        /*sout+enter*/
        /*소괄호 안의 내용이 연산가능하면 연산결과가 나온다*/
        System.out.println(5 + 5 - 1);
        /*쌍따옴표에 작성된 모든 내용은 단순 문자로 인식한다.*/

        System.out.println("5+5");
        System.out.println(3 + "안");
        System.out.println(3+"안녕하세요");
        System.out.println("hi"+"hello");
        /*문자가 하나라도 나오면 문자로 변환된다*/
        System.out.println(3+5+"a"+5);
        /*문자끼리는 더하기만된다 곱하기 나누기 안댐*/
        int mug;
        mug=10;
        /*변수의 초기화를 해주어야한다.*/

    }
}
