
/*반복문
* (while, for)
* 특정 코드를 원하는 횟수만큼 반복적으로 실행하는 코드*/
/*반복 시작 조건*/
/*while(반복 조건){반복 실행할 코드 여러줄 가능} 형태*/
/*+ 반복을 파기할 조건*/

public class While_01 {
    public static void main(String[] args) {
    int num = 0; // 반복시작조건
    while(num < 83){ //반복조건
        System.out.println("15");
        num++; //반복을 파기할 조건 중괄호 안에서 위치 상관 없
    }


    }

}
